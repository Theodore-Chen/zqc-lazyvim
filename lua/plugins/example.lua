            for _, component in ipairs(opts.sections.lualine_c) do
                if type(component) == "table" and component[1] == "filename" then
                    component.path = 4
                    component.shorting_target = 180
                    break
                end
            end
            return opts
        end,
    },

    -- use mini.starter instead of alpha
    { import = "lazyvim.plugins.extras.ui.mini-starter" },

    -- add jsonls and schemastore packages, and setup treesitter for json, json5 and jsonc
    { import = "lazyvim.plugins.extras.lang.json" },

    -- add any tools you want to have installed below
    {
        "williamboman/mason.nvim",
        opts = {
            ensure_installed = {
                "stylua",
                "lua_ls",
                "shellcheck",
                "shfmt",
                "flake8",
            },
        },
    },

    {
        "neovim/nvim-lspconfig",
        opts = {
            servers = {
                luals = {
                    settings = {
                        Lua = {
                            runtime = {
                                version = "LuaJIT",
                            },
                            diagnostics = {
                                globals = { "vim" },
                            },
                            workspace = {
                                library = vim.api.nvim_get_runtime_file("", true),
                                checkThirdParty = false, -- ÈÅøÂÖçÊ£ÄÊü•Á¨¨‰∏âÊñπÂ∫ìÔºàÊØîÂ¶ÇÊèí‰ª∂ÔºâÁöÑË≠¶Âëä
                            },
                            type = {
                                castNumberToInteger = true,
                            },
                        },
                    },
                },
            },
        },
    },
